#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	num;

	if (n < 0)
	{
		write(fd, "-", 1);
		if (n == INT_MIN)
		{
			write(fd, "2", 1);
			n = 147483648;
		}
		else
		{
			n = -n;
		}
	}
	if (n / 10 != 0)
		ft_putnbr_fd(n / 10, fd);
	num = (n % 10) + '0';
	write(fd, &num, 1);
}
