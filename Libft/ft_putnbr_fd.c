#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	num;

	if (n / 10 < 0 && n / 10 > -10)
		write(1, "-", 1);
	if (n / 10 > 0 | n / 10 < 0)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	num = n % 10;
	if (num < 0)
		num *= -1;
	num += 48;
	write(fd, &num, 1);
}
