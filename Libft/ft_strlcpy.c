#include "libft.h"

size_t	ft_strlcpy(char *d, const char *s, size_t n)
{
	size_t	len;

	len = 0;
	while (*s && n)
	{
		*d++ = *s;
		s++;
		n--;
		len++;
	}
	*d = 0;
	return (len);
}
